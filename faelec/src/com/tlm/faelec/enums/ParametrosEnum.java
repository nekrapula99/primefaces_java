package com.tlm.faelec.enums;


public enum ParametrosEnum {	
	
	EMAIL_ASUNTO				("EMAIL_ASUNTO", null),
	EMAIL_CUERPO				("EMAIL_CUERPO", null);
	   
	ParametrosEnum (String codigo, Object valor) { 
        this.codigo = codigo;
        this.valor = valor;
    } 

	private String codigo;
	private Object valor;
	

	public String getCodigo() {
		return codigo;
	}

	public Object getValor() {
		return valor;
	}
        
    public void setValor(Object valor) {
            this.valor = valor;
	}
    
    
    
}
